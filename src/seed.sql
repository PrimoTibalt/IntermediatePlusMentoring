            INSERT INTO public."Rows" ("SectionId", "Number") VALUES (section_id, row_number);
        END LOOP;
    END LOOP;
END $$;

-- Seed data for Seats
DO $$
DECLARE
    row_id integer;
    seat_number integer;
BEGIN
    FOR row_id IN (SELECT "Id" FROM public."Rows") LOOP
